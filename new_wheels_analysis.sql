-- Question 1: Find the total number of customers who have placed orders. What is the distribution of the customers across states?

-- Part 1
SELECT 
    COUNT(DISTINCT customer_id) AS Customers_Who_Placed_Orders
FROM order_t;

-- Part 2
SELECT 
    c.state,
    COUNT(DISTINCT customer_id) AS num_customers 
FROM customer_t c 
    JOIN order_t o USING(customer_id)
WHERE quantity IS NOT NULL
GROUP BY 
    c.state
ORDER BY 
    num_customers DESC;


-- Question 2: Which are the top 5 vehicle makers preferred by the customers?

SELECT  
    p.vehicle_maker,
    COUNT(DISTINCT customer_id) AS num_customers
FROM product_t p 
    JOIN order_t o USING(product_id)
    JOIN customer_t c USING(customer_id)
GROUP BY 
    p.vehicle_maker
ORDER BY
    num_customers DESC
LIMIT 5;



-- Question 3: Which is the most preferred vehicle maker in each state?

SELECT  
    state,
    vehicle_maker,
    customer_count
FROM (
    SELECT 
        c.state,
        p.vehicle_maker,
        COUNT(DISTINCT c.customer_id) AS customer_count,
        RANK() OVER(PARTITION BY c.state ORDER BY COUNT(DISTINCT c.customer_id) DESC) AS rnk
    FROM
    product_t p 
    JOIN order_t o USING(product_id)
    JOIN customer_t c USING(customer_id)
    GROUP BY 
        c.state,
        p.vehicle_maker)
WHERE rnk = 1
ORDER BY state;

-- Question 4: Find the overall average rating given by the customers. What is the average rating in each quarter?

-- Part 1: Finding the Overall Average Rating given by customers
SELECT  
    ROUND(AVG(feedback_num_value), 2) as Overall_Avg_Rating
FROM (
    SELECT
        o.quarter_number AS QuarterNumber,
        CASE 
            WHEN o.customer_feedback = 'Very Bad' THEN 1
            WHEN o.customer_feedback = 'Bad' THEN 2
            WHEN o.customer_feedback = 'Okay' THEN 3
            WHEN o.customer_feedback = 'Good' THEN 4
            WHEN o.customer_feedback = 'Very Good' THEN 5
            ELSE NULL
        END AS feedback_num_value 
    FROM order_t o 
) AS Customer_Feedback_Scores;

-- Part 2: The Average Rating in each quarter
SELECT  
    QuarterNumber,
    ROUND(AVG(feedback_num_value), 2) as Quarterly_Avg_Rating
FROM (
    SELECT
        o.quarter_number AS QuarterNumber,
        CASE 
            WHEN o.customer_feedback = 'Very Bad' THEN 1
            WHEN o.customer_feedback = 'Bad' THEN 2
            WHEN o.customer_feedback = 'Okay' THEN 3
            WHEN o.customer_feedback = 'Good' THEN 4
            WHEN o.customer_feedback = 'Very Good' THEN 5
            ELSE NULL
        END AS feedback_num_value 
    FROM order_t o 
) AS Customer_Feedback_Scores
GROUP BY 
    QuarterNumber 
ORDER BY 
    QuarterNumber;

-- Question 5: Find the percentage distribution of feedback from the customers. Are customers getting more dissatisfied over time?

SELECT  
    QuarterNumber,
    ROUND(SUM(CASE WHEN feedback_num_value = 1 THEN 1 ELSE 0 END) * 100.0 /COUNT(feedback_num_value), 2) AS 'Very Bad(%)',	
    ROUND(SUM(CASE WHEN feedback_num_value = 2 THEN 1 ELSE 0 END) * 100.0 /COUNT(feedback_num_value), 2) AS 'Bad (%)',
    ROUND(SUM(CASE WHEN feedback_num_value = 3 THEN 1 ELSE 0 END) * 100.0 /COUNT(feedback_num_value), 2) AS 'Okay (%)',
    ROUND(SUM(CASE WHEN feedback_num_value = 4 THEN 1 ELSE 0 END) * 100.0 /COUNT(feedback_num_value), 2) AS 'Good (%)',
    ROUND(SUM(CASE WHEN feedback_num_value = 5 THEN 1 ELSE 0 END) * 100.0 /COUNT(feedback_num_value), 2) AS 'Very Good (%)'
FROM (
    SELECT
        o.quarter_number AS QuarterNumber,
        CASE 
            WHEN o.customer_feedback = 'Very Bad' THEN 1
            WHEN o.customer_feedback = 'Bad' THEN 2
            WHEN o.customer_feedback = 'Okay' THEN 3
            WHEN o.customer_feedback = 'Good' THEN 4
            WHEN o.customer_feedback = 'Very Good' THEN 5
            ELSE NULL
        END AS feedback_num_value 
    FROM order_t o 
) AS Customer_Feedback_Scores

GROUP BY 
    QuarterNumber 
ORDER BY 
    QuarterNumber; 

-- Question 6: What is the trend of the number of orders by quarter?

SELECT
