-- Question 7: Calculate the net revenue generated by the company. What is the quarter-over-quarter % change in net revenue?

-- Part 1: Find the net revenue generated by the company
SELECT
    ROUND(SUM(quantity * vehicle_price * (1 - (discount / 100))), 2)
        AS Total_Net_Revenue
FROM order_t;

-- Part 2: QoQ % Change in Net Revenue

SELECT
    Sales_Quarter,
    ROUND(Net_Revenue, 2) AS Net_Revenue,
    ROUND(LAG(Net_Revenue) OVER(ORDER BY Sales_Quarter), 2) AS Previous_Quarter_Revenue,
    ROUND((Net_Revenue - LAG(Net_Revenue) OVER (ORDER BY Sales_Quarter)) * 100.0
    / LAG(Net_Revenue) OVER (ORDER BY Sales_Quarter),2) AS QoQ_Percentage_Change
FROM (
    SELECT 
        quarter_number AS Sales_Quarter,
        SUM(quantity*vehicle_price*(1-(discount / 100))) AS Net_Revenue
    FROM 
        order_t 
    GROUP BY 
        quarter_number
) AS QuarterlyRevenue

ORDER BY 
    Sales_Quarter;


-- Question 8: What is the trend of net revenue and orders by quarters?

SELECT
    quarter_number AS Sales_Quarter,
    ROUND(
        SUM(quantity*vehicle_price*(1-(discount / 100))),2) AS Net_Revenue,
    COUNT(order_id) AS Total_Orders
FROM 
    order_t 
GROUP BY 
    quarter_number

ORDER BY 
    Sales_Quarter;

-- Question 9: What is the average discount offered for different types of credit cards?

SELECT
    credit_card_type AS Card_Type,
    ROUND(AVG(o.discount), 2) AS Average_Card_Discount
FROM customer_t c 
    JOIN order_t o USING(customer_id)
GROUP BY
    Card_Type
ORDER BY
    Average_Card_Discount DESC;

-- Question 10: What is the average time taken to ship the placed orders for each quarter?
SELECT
    quarter_number AS Quarter,
    ROUND(AVG(JULIANDAY(ship_date)) - (JULIANDAY(order_date)),2) AS Average_Days_Since_Order
FROM 
    order_t
GROUP BY
    quarter_number
ORDER BY
    Quarter;
    
    
